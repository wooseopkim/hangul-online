<template>
  <main class="content">
    <font-card
      v-once
      v-for="font in fonts"
      :model="font"
      :toggle="toggle"
      :key="font.name.en"
    />
  </main>
</template>

<script>
import fonts from '../assets/fonts/data.json'
import FontCard from './FontCard.vue'

export default {
  props: ['store'],

  components: {
    FontCard
  },

  data() {
    const toggle = (item) => {
      this.store.onToggle(item)
    }

    return {
      fonts: fonts.sort((a, b) => a.name.ko.localeCompare(b.name.ko)),
      toggle
    }
  }
}
</script>

